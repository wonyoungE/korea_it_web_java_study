package _21_Class.OOP;

public class OOP {
    public static void main(String[] args) {
        // OOP : Object Oriented Programming
        // 객체 지향 프로그래밍
        // - 현실 세계의 개념을 코드로 표현
        // - 사람, 동물, 자동차 등을 하나의 객체로 보는 것
        // - 상태(속성)과 동작(기능)을 중심으로 프로그래밍
        // - 속성 = 필드, 기능 = 메서드

        // *** OOP의 4가지 원칙 ***
        // 1) 추상화
        //    - 복잡한 내부 동작은 감추고 꼭 필요한 기능만 보여주는 것
        //    - 객체에 있는 메서드를 호출해서 사용하는 것 (메서드가 어떻게 돌아가는지 몰라도 호출만 하면 사용 가능)
        //    - ex) car.drive()
        // 2) 캡슐화
        //    - 정보 은닉
        // 3) 상속
        // 4) 다형성

        // 접근 제어자 - 정보 은닉
        // OOP에서는 private를 많이 씀
        // public       - 어디서든 접근 가능
        // default      - 같은 패키지 내에서만 접근 가능 (명시를 따로 하지 않으면 default)
        // protected    - 같은 패키지 또는 자식 클래스에서만 접근 가능
        // private      - 해당 클래스 내부에서만 접근 가능
    }
}
